// operadores aritmeticos basicos 

let counter = 200;

// suma 

counter = counter + 20;
console.log(counter);
counter +=20;
console.log(counter);

// multiplicacion


counter = counter * 20;
console.log(counter);
counter *=20;
console.log(counter);

// resta
counter = counter - 20;
console.log(counter);
counter -=20;
console.log(counter);

//division 
counter = counter / 20;
console.log(counter);
counter /=20;
console.log(counter);
